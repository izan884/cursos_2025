print('*** Operador Asignacion compuesta ***')
a, b = 10, 15
print(f'a: {a}, b: {b}')
#operador compuesto de suma +=
a += b
print(f'operador a: {a}, b es: {b}')
#operador compuesto de Resta -=
a = 10
a -= b
print(f'operador a: {a}')

#operador compuesto de multiplicacion
a = 10
a *= b
print(f'operador multiplicacion a: {a}')

#operador compuesto de division
a = 10
a /= b
print(f'operador division a: {a:.2f}')