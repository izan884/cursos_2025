# Operadores Arimeticos
a = 10
b= 3
#suma +
suma = a + b
print(f'Suma :  {suma}')

#resta -
resta = a - b
print(f'Resta : {resta}')

#Multiplicacion
multiplicacion = a * b
print(f'Multiplicacion : {multiplicacion}')
division = a / b
print(f'Division : {division:.2f}')

#division_entera
division_entera = a // b
print(f'Division entera : {division_entera}')

#Modulo (%) residuo de la division
modulo = a % b
print(f'Residuo : {modulo}')
# Exponente (potencia) **
exponente = a ** b
print(f'exponente : {exponente}') # 10 elevado a la 3 = 10 = 10*10*10= 1000

